package Conditional_Statement_if_else;

public class else_if_Example {
		public static void main(String[] args) {
			if (false) {
				System.out.println("If code executed");
			}else if (true)
			{
				System.out.println("first else if block code executed");
			}else if (true)
			{
				System.out.println("second else if block code executed");
			}else {
				System.out.println("default block of code");
			}
		}
}
